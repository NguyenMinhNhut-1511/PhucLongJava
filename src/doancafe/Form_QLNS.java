    private boolean _CheckUserExist(String user){
        boolean IsExist = false;
        try {
           Statement stmt = conn.createStatement();
           ResultSet rs = stmt.executeQuery("Select ID from TaiKhoan Where username='" + user +"'");
           if (rs.next()){
                IsExist = true;
           }else{
                IsExist = false;
           }      
        } catch (SQLException ex) {
            Logger.getLogger(Form_Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        return IsExist;
    }
    
    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        if (cothem){
            // THEM NHAN VIEN
            String username = txtUserName.getText();
            String email = txtEmail.getText();
            String hoTen = txtHoTen.getText();
            String gioiTinh = cbGioiTinh.getSelectedItem().toString();
            String ngaySinh = txtNgaySinh.getText();
            String SDT = txtSDT.getText();
            String diaChi = txtDiaChi.getText();
            String chucVu = cbChucVu.getSelectedItem().toString();
            if (_CheckUserExist(username)){
                JOptionPane.showMessageDialog(null, "Tên Đăng Nhập Đã Tồn Tại !","Thông Báo",JOptionPane.ERROR_MESSAGE);
                return;
            }else{
                String sql = "INSERT INTO NhanVien (email, hoTen, gioiTinh, namSinh, sdt, diaChi, chucvu, trangthai) VALUES (?,?,?,?,?,?,?,0)";
                PreparedStatement stmt;
                try {
                    String generatedColumns[] = { "ID" };
                    stmt = conn.prepareStatement(sql,generatedColumns);
                    stmt.setString(1, txtEmail.getText());
                    stmt.setString(2, txtHoTen.getText());
                    stmt.setString(3, cbGioiTinh.getSelectedItem().toString());
                    stmt.setString(4, txtNgaySinh.getText());
                    stmt.setString(5, txtSDT.getText());
                    stmt.setString(6, txtDiaChi.getText());
                    stmt.setString(7, cbChucVu.getSelectedItem().toString().contains("Quản Lý") ? "admin" : "nhanvien");
                    // Thực hiện lệnh SQL
                    stmt.executeUpdate();
                    ResultSet rs = stmt.getGeneratedKeys();
                    if (rs.next()) {
                        long ID = rs.getLong(1);
                        String sql2 = "INSERT INTO TaiKhoan (username, matKhau, ID) VALUES (?,?,?)";
                        stmt = conn.prepareStatement(sql2);
                        stmt.setString(1, username);
                        stmt.setString(2, "123456");
                        stmt.setString(3, Long.toString(ID));
                        stmt.executeUpdate();
                        JOptionPane.showMessageDialog(null, "Đã Tạo Tài Khoản Thành Công !","Thông Báo",JOptionPane.INFORMATION_MESSAGE);
                        JOptionPane.showMessageDialog(null, "Mật Khẩu Của Bạn Là: 123456\nVui Lòng Đổi Mật Khẩu!!!","Thông Báo",JOptionPane.INFORMATION_MESSAGE);
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(Form_QLNS.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }else{
            // SUA NHAN VIEN
            String sql = "UPDATE NhanVien SET email=?,hoTen=?,gioiTinh=?,namSinh=?,sdt=?,diaChi=?,chucvu=? WHERE ID=?";
            PreparedStatement stmt;
            try {
                stmt = conn.prepareStatement(sql);
                stmt.setString(1, txtEmail.getText());
                stmt.setString(2, txtHoTen.getText());
                stmt.setString(3, cbGioiTinh.getSelectedItem().toString());
                stmt.setString(4, txtNgaySinh.getText());
                stmt.setString(5, txtSDT.getText());
                stmt.setString(6, txtDiaChi.getText());
                stmt.setString(7, cbChucVu.getSelectedItem().toString().contains("Quản Lý") ? "admin" : "nhanvien");
                stmt.setString(8, txtID.getText());
                // Thực hiện lệnh SQL
                stmt.executeUpdate();
            } catch (SQLException ex) {
                Logger.getLogger(Form_QLNS.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        _LoadDS();
        _SetEnable(true);
        _SetNull();
    }//GEN-LAST:event_btnLuuActionPerformed

