    private void _LoadDS(){
        DefaultTableModel tMOdel = (DefaultTableModel) listNV.getModel();
        tMOdel.setRowCount(0);
        try {
           Statement stmt = conn.createStatement();
           ResultSet rs = stmt.executeQuery("SELECT * FROM NhanVien");
           while (rs.next()){
                if (rs.getString(9).contains("0")){
                    String ID = rs.getString(1);
                    String name = _get1String("SELECT username FROM TaiKhoan WHERE ID = '" + ID +"'");
                    String email = rs.getString(2);
                    String hoTen = rs.getString(3);
                    String gioiTinh = rs.getString(4);
                    String namSinh = rs.getString(5);
                    String sdt = rs.getString(6);
                    String diaChi = rs.getString(7);
                    String chucvu = rs.getString(8).contains("admin") ? "Quản Lý" : "Nhân Viên";
                    DefaultTableModel model = (DefaultTableModel)listNV.getModel();
                    model.addRow(new Object[]{ID,name,email,hoTen,gioiTinh,namSinh,sdt,diaChi,chucvu});
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(Form_Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
