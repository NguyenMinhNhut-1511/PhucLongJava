    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        IsClick = !IsClick;
        if (IsClick){   
            // Đã Nhấn
            _SetEnable(true);
            btnSua.setEnabled(true);
            btnSua.setText("Lưu Thông Tin");
        }else{
            // Đã Lưu
            if (txtEmail.getText().length() == 0 || txtHoTen.getText().length() == 0 || cbGioiTinh.getSelectedItem().toString().length() == 0 || txtNamSinh.getText().length() == 0 || txtSDT.getText().length() == 0 || txtDiaChi.getText().length() == 0){
                JOptionPane.showMessageDialog(null, "Vui Lòng Nhập Đúng Thông Tin!!","Thông Báo",JOptionPane.ERROR_MESSAGE);
                return;
            }
            String sql = "UPDATE NhanVien SET email=?,hoTen=?,gioiTinh=?,namSinh=?,sdt=?,diaChi=? WHERE ID=?";
            PreparedStatement stmt;
            try {
                stmt = conn.prepareStatement(sql);
                stmt.setString(1, txtEmail.getText());
                stmt.setString(2, txtHoTen.getText());
                stmt.setString(3, cbGioiTinh.getSelectedItem().toString());
                stmt.setString(4, txtNamSinh.getText());
                stmt.setString(5, txtSDT.getText());
                stmt.setString(6, txtDiaChi.getText());
                stmt.setString(7, ID);
                // Thực hiện lệnh SQL
                stmt.executeUpdate();
            } catch (SQLException ex) {
                Logger.getLogger(Form_QLNS.class.getName()).log(Level.SEVERE, null, ex);
            }
            _SetEnable(false);
            btnSua.setText("Sửa Thông Tin");
            _LoadInfor();
        }
    }//GEN-LAST:event_btnSuaActionPerformed

