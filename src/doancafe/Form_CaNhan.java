    private void _LoadInfor(){
        try {
           Statement stmt = conn.createStatement();
           ResultSet rs = stmt.executeQuery("SELECT * FROM NhanVien WHERE ID=" + ID);
           if(rs.next()){
                String email = rs.getString(2);
                txtEmail.setText(email);
                String hoTen = rs.getString(3);
                txtHoTen.setText(hoTen);
                String gioiTinh = rs.getString(4);
                cbGioiTinh.setSelectedItem(gioiTinh);
                String namSinh = rs.getString(5);
                txtNamSinh.setText(namSinh);
                String sdt = rs.getString(6);
                txtSDT.setText(sdt);
                String diaChi = rs.getString(7);
                txtDiaChi.setText(diaChi);
           }
        } catch (SQLException ex) {
            Logger.getLogger(Form_Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
