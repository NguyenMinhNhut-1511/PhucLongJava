    private void _LoadDS(){
        DefaultTableModel tMOdel = (DefaultTableModel) list.getModel();
        tMOdel.setRowCount(0);
        try {
           Statement stmt = conn.createStatement();
           ResultSet rs = stmt.executeQuery("SELECT * FROM PhieuXuat");
           while (rs.next()){
                    String maPhieuXuat = rs.getString(1);
                    String name = _get1String("SELECT hoTen FROM NhanVien as A , TaiKhoan as B WHERE A.ID = B.ID and username = '" + rs.getString(2) + "'");
                    String tienThu = rs.getString(3);
                    String tienTra = rs.getString(4);
                    String uuDai = rs.getString(5);
                    String thanhToan = rs.getString(6);
                    String ngayPhieu = rs.getString(7);
                    tMOdel.addRow(new Object[]{maPhieuXuat,name, tienThu, tienTra, uuDai, thanhToan, ngayPhieu});
            }
        } catch (SQLException ex) {
            Logger.getLogger(Form_Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
