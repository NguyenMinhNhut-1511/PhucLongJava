    private void listMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listMouseClicked
        // TODO add your handling code here:
        JTable source = (JTable)evt.getSource();
        int row = source.rowAtPoint(evt.getPoint());
        int column = source.columnAtPoint( evt.getPoint() );
        String maPhieuXuat = source.getModel().getValueAt(row, 0)+"";
        DefaultTableModel tMOdel = (DefaultTableModel) list2.getModel();
        tMOdel.setRowCount(0);
        try {
           Statement stmt = conn.createStatement();
           ResultSet rs = stmt.executeQuery("SELECT * FROM CTPhieuXuat WHERE maPhieuXuat = '" + maPhieuXuat + "'");
           while (rs.next()){
                String maSP = rs.getString(3);
                String name = _get1String("SELECT tenSP FROM SanPham WHERE maSP = '" + maSP + "'");
                String SL = rs.getString(4);
                String Gia = Integer.toString(Integer.parseInt(_get1String("SELECT Gia FROM SanPham WHERE maSP = '" + maSP + "'")) * Integer.parseInt(SL));
                tMOdel.addRow(new Object[]{maSP,name,SL,Gia});
            }
        } catch (SQLException ex) {
            Logger.getLogger(Form_Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_listMouseClicked

